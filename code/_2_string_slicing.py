variable_string = 'Hello this a python program'

# Slicing
sliced_string = variable_string[0]
print(sliced_string)
sliced_string = variable_string[1]
print(sliced_string)
sliced_string = variable_string[-1]
print(sliced_string)
sliced_string = variable_string[0:4]
print(sliced_string)
sliced_string = variable_string[1:4]
print(sliced_string)
sliced_string = variable_string[0:5]  # The 2nd index number wont be inclusive
print(sliced_string)
sliced_string = variable_string[0:]  
print(sliced_string)
sliced_string = variable_string[0:5:2]  
print(sliced_string)